fps = 10

def draw_grid(screen, grid, cell_size):
    for y, row in enumerate(grid):
        for x, cell in enumerate(row):
            color = (0, 0, 0) if cell == 1 else (255, 255, 255)
            rect = pygame.Rect(x * cell_size, y * cell_size, cell_size, cell_size)
            pygame.draw.rect(screen, color, rect)

running = True
while running:
    for event in pygame.event.get():
        if event.type == pygame.QUIT:
            running = False
    
    screen.fill((0, 0, 0))  # Optional: fill the screen with black before drawing the grid
    
    draw_grid(screen, grid, cell_size)
    grid = get_next_state(grid)
    
    pygame.display.flip()  # Update the display

    clock.tick(fps)

